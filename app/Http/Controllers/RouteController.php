    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($name)
    {
        $route = DB::table('routes')
            ->where('name', '=', $name)
            ->get();

        if( $route->count() ) {
            return view('pages.route-single', compact('route'));
        }else {
            abort(404, 'Route Not Found');
            return view('errors.404');
        }       
    }

